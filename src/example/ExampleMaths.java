package example;

public class ExampleMaths {

    public int add(int x, int y) {
        return x + y;
    }

    public int subtract(int x, int y) {
        return x - y;
    }

    public int divide(int x, int y) {
        return x / y;
    }

    public boolean isBiggerThan(int x, int y) {
        return x >= y;
    }
}
